std::string cid2color(Color color_id)
{
	switch(color_id)
	{
    case Color::RED:
        return "red";
    case Color::BLUE:
        return "blue";
    case Color::GREEN:
        return "green";
    case Color::PINK:
        return "pink";
    case Color::ORANGE:
        return "orange";
    case Color::YELLOW:
        return "yellow";
    case Color::BLACK:
        return "black";
    case Color::WHITE:
        return "white";
    case Color::PURPLE:
        return "purple";
    case Color::BROWN:
        return "brown";
    case Color::CYAN:
        return "cyan";
    case Color::LIME:
        return "lime";
    default:
        return "unknown";
	}
}

void hSetPlayerColor(void* something, unsigned char player_id, unsigned char color)
{
    player_colors[player_id] = (Color)color;
    oSetPlayerColor(something, player_id, color);
}

template<typename FnPtr>
FnPtr install_hook(FnPtr original, FnPtr hook)
{
    return (FnPtr)untyped_install_hook((void*)original, (void*)hook);
}

